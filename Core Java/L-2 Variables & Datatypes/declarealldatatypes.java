/* Try to declare meaningful variables of each type e.g. a variable named age shuld be a numeric type(int or float) not byte */

public class declarealldatatypes {
    public static void main(String[] args) {
        byte age = 25;
        short salary = 30000;
        int india_population = 1400000000;
        long aadhar_no = 582025605772L;
        char letter = 'A';
        boolean areyouadult = true;
        float CGPA = 7.90f;
        double pi = 3.14872351648936;

        System.out.println(age);
        System.out.println(salary);
        System.out.println(india_population);
        System.out.println(aadhar_no);
        System.out.println(letter);
        System.out.println(areyouadult);
        System.out.println(CGPA);
        System.out.println(pi);    
    }
}
